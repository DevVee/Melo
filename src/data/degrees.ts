/** Infer education level from a stored degree string */
export function inferLevel(degree: string): EduLevel | '' {
  if (!degree) return ''
  const d = degree.toLowerCase()
  if (d.includes('elementary') || d.includes('primary') || d.includes('grade 6')) return 'elementary'
  if (d.includes('junior high') || d.includes('grade 10') || d.includes('lower secondary')) return 'jhs'
  if (d.includes('senior high') || d.includes('grade 12') || d.includes('k-12') || d.includes('shs') || d.includes('stem') || d.includes('abm') || d.includes('humss') || d.includes('tvl') || d.includes('gas strand')) return 'shs'
  if (d.includes('tesda') || d.includes('nc i') || d.includes('nc ii') || d.includes('nc iii') || d.includes('nc iv') || d.includes('certificate in') || d.includes('diploma in') || d.includes('vocational') || d.includes('technical-vocational')) return 'vocational'
  if (d.includes('master')) return 'masters'
  if (d.includes('doctor') || d.includes('phd') || d.includes('juris') || d.includes('md)') || d.includes('dds') || d.includes('dvm') || d.includes('pharmd') || d.includes('dnp') || d.includes('dba')) return 'doctorate'
  if (d.includes('bachelor') || d.includes('associate') || d.includes('bs ') || d.includes('bsit') || d.includes('bscs') || d.includes('bsn') || d.includes('llb') || d.includes('bfa')) return 'college'
  return ''
}
